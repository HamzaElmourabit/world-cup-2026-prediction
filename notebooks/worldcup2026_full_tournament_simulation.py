def add_features(df, team_stats):

    df = df.copy()

    df = df.merge(
        team_stats[["team", "elo"]].rename(columns={"elo": "home_elo"}),
        left_on="home_team",
        right_on="team",
        how="left"
    ).drop("team", axis=1)

    df = df.merge(
        team_stats[["team", "elo"]].rename(columns={"elo": "away_elo"}),
        left_on="away_team",
        right_on="team",
        how="left"
    ).drop("team", axis=1)

    df["elo_difference"] = df["home_elo"] - df["away_elo"]

    return df

